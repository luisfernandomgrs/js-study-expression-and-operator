// Operadores de atribuição (Assignment)

let x;

// undefined... Não teve atribuição
console.log(x);

// assignment
x = 1; console.log(x);

// additional assignment
x += 2; console.log(x);

// subtraction assignment
x -= 1;
console.log(x);

// multiplication assignment
x *= 2; console.log(x);

// division assignment
x /= 2; console.log(x);

// remainder assignment
x %= 2; console.log(x);

// exponentiation assignment
x = 3;
x **= 2; console.log(x);